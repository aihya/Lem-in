#include "libft.h"

int     ft_strisempty(char *str)
{
	int     i;

	i = 0;
	while (str[i])
	{
		if (!ft_isprint(str[i]))
			return (0);
		i++;
	}
	if (i == 0)
		return (0);
	return (1);
}